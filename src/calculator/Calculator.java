    public void bateryCheck() {
        if (batery > 75 && batery <= 100) {
            verde.setVisible(true);
            galben.setVisible(false);
            maro.setVisible(false);
            rosu.setVisible(false);
        } else if (batery > 50 && batery < 75) {
            verde.setVisible(false);
            galben.setVisible(true);
            maro.setVisible(false);
            rosu.setVisible(false);
        } else if (batery > 25 && batery < 50) {
            verde.setVisible(false);
            galben.setVisible(false);
            maro.setVisible(true);
            rosu.setVisible(false);
        } else if (batery > 0 && batery <= 25) {
            verde.setVisible(false);
            galben.setVisible(false);
            maro.setVisible(false);
            rosu.setVisible(true);
        }
    }
    
