    public void calculeaza() {
        switch (operatie) {
            case 1:
                rezultat = numarActual + Integer.parseInt(textAfisat.getText());
                break;
            case 2:
                rezultat = numarActual - Integer.parseInt(textAfisat.getText());
                break;
            case 3:
                rezultat = numarActual * Integer.parseInt(textAfisat.getText());
                break;
            case 4:
                rezultat = numarActual / Integer.parseInt(textAfisat.getText());
                break;
            case 5:
                String[] rezRadical = textAfisat.getText().split(" ");
                rezultat = Math.sqrt(Double.parseDouble(rezRadical[2]));
                
                break;
            case 6:
                String[] rezPutere = textAfisat.getText().split(" ");
                rezultat = Math.pow(numarActual, Double.parseDouble(rezPutere[3]));
                break;
            case 7:
                String[] rezFactorial = textAfisat.getText().split(" ");
                rezultat = 1;
                for (int i = 2; i <= Integer.parseInt(rezFactorial[0]); i++) {
                    rezultat *= i;
                }
            
        }
    }
    
